    Step "Compiling Sapphire.exe ..."
    & g++ Sapphire.cpp @CommonFlags -o Sapphire.exe
    Check "Sapphire.exe"
}

# в”Җв”Җ 4. Installer в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
